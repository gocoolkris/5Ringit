package frontEndObject;

public class AtheleteObject {

	private String attName, attValue;
	
	public AtheleteObject(String aName, String value){
		attName = aName;
		attValue = value;
	}
	
	
	public String getAttributeName(){
		return attName;
	}
	
	public String getAttributeValue(){
		
		return attValue;
	}
	
	
	
	
}
